    private void deleteLikedSongBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteLikedSongBtnActionPerformed
        //removes last song enter from user
        if (count > 0) {
            myList.removeSong(myList.playlistSize());
            count--;
            outputBox.setText(myList.printAllSongs());
        } else if (count == 0) {
            outputBox.setText("Your liked songs playlist is already empty.");
        }
    }//GEN-LAST:event_deleteLikedSongBtnActionPerformed

    private void searchLikedSongBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchLikedSongBtnActionPerformed
        String userInput = addSongTextField.getText();
        boolean found = false;
        int position = -1;
        //loops through songs to find the same one searched by user
        for (int i = 0; i < count; i++) {
            if (myList.getSongs(i).equals(userInput)) {
                found = true;
                position = i + 1;
                break;
            }
        }
        
        if (found) {
            outputBox.setText(userInput + " found at position " + position + " in the list.");
        } else if (addSongTextField.getText().isEmpty()) {
            outputBox.setText("There is no song to find in your liked Playlist.");
        } else {
            outputBox.setText(userInput + " not found in the list.");
        }
    }//GEN-LAST:event_searchLikedSongBtnActionPerformed

