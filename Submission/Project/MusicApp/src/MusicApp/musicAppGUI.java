    private void addLikedSongBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLikedSongBtnActionPerformed
        String userInput = addSongTextField.getText();
        if (userInput.isBlank()) {
            outputBox.setText("Please enter a valid song name.");
        } else {
            myList.addSong(count, userInput);
            count++;
            outputBox.setText(userInput + " is added to your liked Playlist.");
        }
    }//GEN-LAST:event_addLikedSongBtnActionPerformed

